
package com.mycompany.bonvino.logica;

public class Vino {
    String anada;
    String imagenEtiqueta;
    String nombre;
    String notaDeCataBodega;
    int precioARS;

    public Vino(String anada,String nombre, String imagenEtiqueta, String notaDeCataBodega, int precioARS){
        this.anada = anada;
        this.imagenEtiqueta = imagenEtiqueta;
        this.notaDeCataBodega = notaDeCataBodega;
        this.nombre = nombre;
        this.precioARS = precioARS;
    }
 
}
